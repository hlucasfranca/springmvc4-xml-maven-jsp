package com.hellokoding.hello.web;

public class Formulario {

    private String objetoUm;

    private String objetoDois;

    private String objetoTres;

    private String objetoQuatro;

    private String texto = null;

    public String getObjetoUm() {
        return objetoUm;
    }

    public void setObjetoUm(String objetoUm) {
        this.objetoUm = objetoUm;
    }

    public String getObjetoDois() {
        return objetoDois;
    }

    public void setObjetoDois(String objetoDois) {
        this.objetoDois = objetoDois;
    }

    public String getObjetoTres() {
        return objetoTres;
    }

    public void setObjetoTres(String objetoTres) {
        this.objetoTres = objetoTres;
    }

    public String getObjetoQuatro() {
        return objetoQuatro;
    }

    public void setObjetoQuatro(String objetoQuatro) {
        this.objetoQuatro = objetoQuatro;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }
}
